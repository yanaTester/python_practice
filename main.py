# Дано натуральное число n. Напишите программу, которая печатает численный треугольник с высотой равной n, в соответствии с примером:
# 1
# 121
# 12321
# 1234321
# 123454321

num = int(input())

for i in range(1, num + 1):
    for j in range(1, i + 1):
        print(j, end="")
    for k in range(i, 1, -1):
        print(k - 1, end="")
    print()

# На вход программе подается натуральное число n. Напишите программу, выводящую графическое изображение делимости чисел от 1 до n включительно.
# В каждой строке надо напечатать очередное число и столько символов «+», сколько делителей у этого числа.

n = int(input())

for i in range(1, n + 1):
    count = ""
    for j in range(1, i + 1):
        if i % j == 0:
            count += "+"
    print(str(i) + count)

